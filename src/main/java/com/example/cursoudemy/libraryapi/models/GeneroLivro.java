package com.example.cursoudemy.libraryapi.models;

/**
 * Enumeração que representa os possíveis gêneros de livros na aplicação.
 * Utilizado para categorizar livros e facilitar consultas e validações baseadas em gênero.
 * Os valores seguem convenções de nomenclatura em português para melhor legibilidade.
 */
public enum GeneroLivro {
    FICCAO,     // Gênero para livros de ficção, incluindo histórias inventadas e narrativas criativas.
    FANTASIA,   // Gênero para livros de fantasia, envolvendo elementos mágicos, mundos imaginários e seres míticos.
    MISTERIO,   // Gênero para livros de mistério, focados em enigmas, investigações e suspense.
    ROMANCE,    // Gênero para livros românticos, centrados em relacionamentos amorosos e emoções.
    BIOGRAFIA,  // Gênero para biografias, narrativas sobre a vida real de pessoas notáveis.
    CIÊNCIA     // Gênero para livros científicos, abordando temas de ciência, tecnologia e descobertas.
}
